    private void btnActualizarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarProductoActionPerformed

        String observaciones;
        float precio;
        int StockDisponible, StockAgregado, validación = 0;

        precio = Float.parseFloat(txtPrecio.getText().trim());
        StockDisponible = Integer.parseInt(txtStockDisponible.getText());
        StockAgregado = Integer.parseInt(cmbAgregarStock.getSelectedItem().toString());
        observaciones = pnObservaciones.getText();

        if (txtPrecio.getText().trim().equals("")) {
            txtPrecio.setBackground(Color.red);
            validación = +1;
        }
        if (observaciones.equals("")) {
            pnObservaciones.setText("Sin observaciones");
        }

        if (validación == 0) {

            try {
                //Realizamos la actualización en la BD de los campos editados por el usuario
                Connection conexión = Conexión.Conexión_BD();
                PreparedStatement consulta = conexión.prepareStatement(
                        "update productos set precio = ?, stock = ?, ultima_modificacion = ?, observaciones = ? "
                        + "where id_producto = ?");

                consulta.setFloat(1, precio);
                consulta.setInt(2, StockDisponible + StockAgregado);
                consulta.setString(3, usuario);
                consulta.setString(4, observaciones);
                consulta.setInt(5, ID_Producto);
                consulta.executeUpdate();
                conexión.close();

                Limpiar();
                Iluminar();
                JOptionPane.showMessageDialog(null, "Actualización exitosa");
                this.dispose();

            } catch (SQLException e) {
                System.err.println("ERROR al actualizar producto. " + e);
                JOptionPane.showMessageDialog(null, "ERROR al actualizar producto, contacte con el administrador");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Debes de llenar todos los campos");
        }

    }//GEN-LAST:event_btnActualizarProductoActionPerformed

