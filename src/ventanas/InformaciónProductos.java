    public InformaciónProductos() {
        initComponents();
        usuario = Login.usuario;
        ID_Producto = GestionarProductos.ID_Producto;

        setSize(550, 500);
        setResizable(false);
        setTitle("Actualización de producto - Sesión de " + usuario);
        setLocationRelativeTo(null);
        setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);

        ImageIcon ImagenFondo = new ImageIcon("src/images/fondo.jpg");
        Icon IconoFondo = new ImageIcon(ImagenFondo.getImage().getScaledInstance(lblFondo.getWidth(), lblFondo.getHeight(),
                Image.SCALE_DEFAULT));
        lblFondo.setIcon(IconoFondo);
        this.repaint();

        //Realizamos la consulta a la base de datos para llenar la información de los controles
        try {
            Connection conexión = Conexión.Conexión_BD();
            PreparedStatement consulta = conexión.prepareStatement(
                    "select * from productos where id_producto = ?");
            consulta.setInt(1, ID_Producto);
            ResultSet ResultadoConsulta = consulta.executeQuery();

            if (ResultadoConsulta.next() == true) {
                txtNombreProducto.setText(ResultadoConsulta.getString("nombre_producto"));
                txtMarca.setText(ResultadoConsulta.getString("marca"));
                txtPrecio.setText(Float.toString(ResultadoConsulta.getFloat("precio")));
                txtÚtimaModificación.setText(ResultadoConsulta.getString("ultima_modificacion"));
                pnObservaciones.setText(ResultadoConsulta.getString("observaciones"));
                txtStockDisponible.setText(ResultadoConsulta.getString("stock"));                
//                cmbAgregarStock.setSelectedItem(ResultadoConsulta.getString("stock"));

                String día;
                int mes, annio;
                día = String.valueOf(ResultadoConsulta.getInt("dia_registro"));
                mes = ResultadoConsulta.getInt("mes_registro");
                annio = ResultadoConsulta.getInt("annio_registro");
                txtFechaRegistro.setText(día + " de " + mes + " del " + annio);
            }

        } catch (SQLException e) {
            System.err.println("ERROR al cargar la información del producto. " + e);
            JOptionPane.showMessageDialog(null, "ERROR al cargar la información del producto, contacte con el administrador");
        }
    }

